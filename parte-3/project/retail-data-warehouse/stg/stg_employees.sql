DROP TABLE IF EXISTS stg.employees;
    
CREATE TABLE stg.employees
                 (
                            id serial primary key,
                          	name varchar(255),
                          	surname varchar(255),
                          	start_date date,
                          	end_date date, 
                          	phone varchar(30), 
                          	country varchar(30), 
                          	province varchar(30), 
                          	store_id smallint, 
                          	position varchar(30)
  
                 );
